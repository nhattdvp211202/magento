namespace Tigren\CustomerGroupCatalog\Controller\Adminhtml\Rule;

class Newrule extends \Magento\Backend\App\Action
{
    /**
     * @var bool|\Magento\Framework\View\Result\PageFactory
     */
    protected $resultPageFactory = false;

    /**
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context        $context,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    )
    {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\ResultInterface|\Magento\Framework\View\Result\Page
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Tigren_CustomerGroupCatalog::rule');
        $id = $this->getRequest()->getParam('id');
        if (!empty($id)) {
            $resultPage->getConfig()->getTitle()->prepend((__('Edit Rule')));
        } else {
            $resultPage->getConfig()->getTitle()->prepend((__('Add New Rule')));
        }


        return $resultPage;
    }
}
